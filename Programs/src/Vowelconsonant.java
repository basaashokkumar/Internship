public class Vowelconsonant {
    public static void main(String[] args) {
        char ch='a';
        if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u'){
            System.out.println("Given alphabet is an vowel");
        }
        else{
            System.out.println("Given Alphabet is a consonant");
        }
    }
}
